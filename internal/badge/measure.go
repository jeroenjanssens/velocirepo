func measureText(s string) float64 {
	var w float64
	for _, r := range s {
		if cw, ok := verdana11[r]; ok {
			w += cw
		} else {
			w += defaultCharWidth
		}
	}
	return w
}

func roundUpToOdd(v int) int {
	if v%2 == 0 {
		return v + 1
	}
	return v
}
